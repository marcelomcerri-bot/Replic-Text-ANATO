export function formatarReferencia(ref: any): string {
  const partes: string[] = []
  
  if (ref.autores) partes.push(ref.autores)
  if (ref.ano) partes.push(`(${ref.ano})`)
  if (ref.titulo) partes.push(`${ref.titulo}.`)
  if (ref.fonte) partes.push(ref.fonte)
  if (ref.pmcid) partes.push(`PMCID: ${ref.pmcid}`)
  if (ref.pmid) partes.push(`PMID: ${ref.pmid}`)
  if (ref.url) partes.push(`Disponível em: ${ref.url}`)
  if (ref.atualizacao) partes.push(`Atualizado em: ${ref.atualizacao}`)
  
  return partes.join(". ")
}
